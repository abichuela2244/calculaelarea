function areadelcirculo(radio){
    var pi=3.141592653589
    return "el área del círulo es:" + pi*(radio**2)
}
console.log(areadelcirculo(9))

function areadeltriangulo(base, altura){
    return "el área del triángulo es:" + (base*altura)/2
}
console.log(areadeltriangulo(8,10))

function areadelcuadrado(lado){
    return "el área del cuadrado es" + (lado**2)
}
console.log(areadelcuadrado(20))

